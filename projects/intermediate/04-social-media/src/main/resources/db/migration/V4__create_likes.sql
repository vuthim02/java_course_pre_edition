-- V4: Create likes table for social media platform
-- Tracks user likes on posts and comments with polymorphic association

CREATE TABLE IF NOT EXISTS likes (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id         BIGINT          NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    likeable_type   VARCHAR(20)     NOT NULL,
    likeable_id     BIGINT          NOT NULL,
    created_at      TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uk_user_like UNIQUE (user_id, likeable_type, likeable_id)
);

CREATE INDEX IF NOT EXISTS idx_likes_user ON likes(user_id);
CREATE INDEX IF NOT EXISTS idx_likes_target ON likes(likeable_type, likeable_id);
